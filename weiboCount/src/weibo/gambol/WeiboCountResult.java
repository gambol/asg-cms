package weibo.gambol;

public class WeiboCountResult {

	private int forwardNum;
	private int coverNum;
	private int coverManNum;
	private int coverWomanNum;
	public int getForwardNum() {
		return forwardNum;
	}
	public void setForwardNum(int forwardNum) {
		this.forwardNum = forwardNum;
	}
	public int getCoverNum() {
		return coverNum;
	}
	public void setCoverNum(int coverNum) {
		this.coverNum = coverNum;
	}
	public int getCoverManNum() {
		return coverManNum;
	}
	public void setCoverManNum(int coverManNum) {
		this.coverManNum = coverManNum;
	}
	public int getCoverWomanNum() {
		return coverWomanNum;
	}
	public void setCoverWomanNum(int coverWomanNum) {
		this.coverWomanNum = coverWomanNum;
	}
	
	
}
